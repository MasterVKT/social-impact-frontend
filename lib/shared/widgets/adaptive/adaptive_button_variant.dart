/// Types de variantes pour les boutons adaptatifs
/// Cette énumération est utilisée par les widgets qui font référence à AdaptiveButton
enum AdaptiveButtonVariant {
  /// Bouton primaire (rempli avec la couleur primaire)
  primary,

  /// Bouton secondaire (contour avec la couleur primaire)
  secondary,

  /// Bouton texte (sans fond ni contour)
  text,
}
